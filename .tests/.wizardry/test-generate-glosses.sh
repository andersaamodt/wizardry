      ;;
  esac
}

test_user_synonym_target_cannot_inject_generated_function() {
  if ! command -v bash >/dev/null 2>&1; then
    test_skip "requires bash"
    return 0
  fi

  tmpdir=$(make_tempdir)
  spellbook="$tmpdir/spellbook"
